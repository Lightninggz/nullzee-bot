class Constant(type):
    _constants = {}

    def __new__(mcs, *args, **kwargs):
        self = super(Constant, mcs).__new__(mcs, *args, **kwargs)
        mcs._constants[self] = args[2]
        return self

    def __getitem__(self, item):
        return self._constants[self][item]


